const replaceTypes = {
  className: {
    regex: classNameRegex,
    replace: list => `className='${list.join(" ")}'`
  },
  cn: {
    regex: cnRegex,
    replace: list => `cn('${list.join(" ")}'`
  },
  backtick: {
    regex: backtickRegex,
    replace: list => `className={\`${list.join(" ")}\`}`
  },
  backtick2: {
    regex: backtickRegex2,
    replace: list => `{\`${list.join(" ")}\`}`
  },
  quote: {
    regex: quoteRegex,
    replace: list => `'${list.join(" ")}'`
  },
  quote2: {
    regex: quote2Regex,
    replace: list => `"${list.join(" ")}"`
  }
}
const type = "backtick2"; // className, cn, backtick

glob("**/*.{js,jsx,tsx,ts}", { cwd: rootDir }, (er, files) => {
  const unmappedClasses = new Set();
  files.forEach((file) => {
    const filePath = path.join(rootDir, file);
    const data = fs.readFileSync(filePath, 'utf-8');

    const regex = replaceTypes[type].regex;
    const result = data.replace(regex, (match, p1) => {
      const classNames = p1.split(" ");
      const prefixedClassNames = classNames.map((className) => {
        // use lookup instead to map to new classNames
        if (!classMapping[className]) {
          unmappedClasses.add(className);
          // console.log(className);
        }
        return classMapping[className] || className;
      });
      return replaceTypes[type].replace(prefixedClassNames);
    });

    // fs.writeFile(filePath, result, 'utf-8', (err) => {
    //   if (err) console.error(err);
    // });
  });
  console.log(unmappedClasses);
});

