const classNameRegex = /className=\n*\t*\s*["']([\w\s->:=#\.,\[\]%&()/]+)["']/g;
const cnRegex = /cn\(\n*\t*\s*["']([\w\s->:=#\.,\[\]%&()/]+)["']/g;
