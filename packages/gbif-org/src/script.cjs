const prefixedClasses = classesToReplace.map(c => {
  // if it starts with -, then it"s a negative
  if (c.startsWith("-")) {
    // the first - should be replaced by -g-
    return c.replace("-", "-g-");
  }

  // if there is a : colon, then add the g- prefix before the last colon
  if (c.includes(":")) {
    let lastColon = c.lastIndexOf(":");
    return c.substring(0, lastColon + 1) + "g-" + c.substring(lastColon + 1);
  }

  return "g-" + c;
});

// create a map that maps the original class to the prefixed class
const classMap = classesToReplace.reduce((map, c, i) => {
  map[c] = prefixedClasses[i];
  return map;
}, {});
